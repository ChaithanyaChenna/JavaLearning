package com.controllingstatements;

public class SwitchStatements3 {
    public static void main(String args[])
    {
        int age=29;
        switch(age)
        {
            case 16:
            System.out.println("age is 16");
            break;
            case 18:
             System.out.println("age is 18");
             break;
            case 20:
            System.out.println("age is 20");
            break;
            default:System.out.println("please give the valid age");

        }
    }
}
